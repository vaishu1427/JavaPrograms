package programs;

public class FormatSpecifier1 {

	public static void main(String[] args) {
		int a=10;
		float f=10.34f;
		String str="Hello World";
		System.out.printf("Hai %d %f %s",a,f,str);  // CONVERSION
		                                            // char  -  c
		                                            // int   -  d,o,x
		                                            // float -  f,e,g  e,g --> Scientific notation f --> float and double 
		                                            // string-  s      

	}

}
